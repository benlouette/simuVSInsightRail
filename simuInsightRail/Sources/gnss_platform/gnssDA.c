/*
 * Gnss_put_32
 *
 * @brief	Write big endian 32 bit value to the GNSS
 *
 * @param	val - 32 bit value to send
 *
 */
static void Gnss_put_32(uint32_t val)
{
	Gnss_put_ch((val >> 24) & 0xFF);
	Gnss_put_ch((val >> 16) & 0xFF);
	Gnss_put_ch((val >> 8) & 0xFF);
	Gnss_put_ch(val & 0xFF);
}

static bool Gnss_write_check_8(uint8_t ch)
{
	Gnss_put_ch(ch);
	uint8_t c = Gnss_get_ch(1000);
	if(ch != c)
	{
		printf("0x%02X\n\n", c);
		return false;
	}
	return true;
}

static bool Gnss_write_check_32(uint32_t addr)
{
	Gnss_put_32(addr);
	for(int i = 3; i >= 0; i--)
	{
		uint8_t c = Gnss_get_ch(1000);
		if(((addr >> (8 * i)) & 0xFF) != c)
		{
			printf("0x%02X\n\n", c);
			return false;
		}
	}
	return true;
}

