/*
 * Gnss_Dump
 *
 * @brief	Display flash header information and contents
 *
 */
void GNSS_dump(void)
{
	uint32_t addr = 0x20000000;

	// Start the MT3333 in testbox mode
	MT3333_Startup(-1);

	do
	{
		// issue StartCmd
		if(!Gnss_startCmd()) break;

		// display flash header information
		if(!Gnss_read32(addr, 0x200, (uint8_t*)__sample_buffer, true)) break;
		headerDisplay((uint8_t*)__sample_buffer);

		// dump first 512 bytes of the flash
		for(int i = 0; i < 0x200; i += 16)
		{
			uint8_t buf[16];

			if(!Gnss_read32(addr + i, 16, buf, true)) break;
			printf(" %08X  ", addr + i);
			for(int j = 0; j < 16; j++)
				printf("%02X ", buf[j]);
			printf("  ");
			for(int j = 0; j < 16; j++)
				printf("%c", (isprint(buf[j]) ? buf[j] : '.'));
			printf("\n");
		}
		printf("\n");
	} while(0);

	// finish up
	Gnss_finish();
}

